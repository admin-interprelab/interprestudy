import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, organization } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');
    
    if (!LOVABLE_API_KEY) {
      throw new Error('LOVABLE_API_KEY is not configured');
    }

    console.log('Received consultation request for organization:', organization);

    // System prompt tailored for medical interpreter ethics consultation
    const systemPrompt = `You are an expert consultant on medical interpreter ethics and standards. You have deep knowledge of:

- IMIA (International Medical Interpreters Association) Code of Ethics
- CCHI (Certification Commission for Healthcare Interpreters) Standards
- NBCMI (National Board of Certification for Medical Interpreters) Standards
- NCIHC (National Council on Interpreting in Health Care) Standards
- CLAS (Culturally and Linguistically Appropriate Services) Standards
- CHIA (California Healthcare Interpreting Association) Standards

Your role is to:
1. Provide clear, accurate guidance on ethical dilemmas
2. Cite specific standards when relevant
3. Simplify complex ethical concepts
4. Offer practical advice for real-world scenarios
5. Support interpreters at all career stages

When responding:
- Be professional yet approachable
- Use clear, jargon-free language when possible
- Provide specific examples when helpful
- Reference the relevant organization's standards
- Consider cultural sensitivity and practical application

${organization ? `Focus particularly on ${organization} standards for this consultation.` : ''}`;

    const response = await fetch('https://ai.gateway.lovable.dev/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${LOVABLE_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'google/gemini-2.5-flash',
        messages: [
          { role: 'system', content: systemPrompt },
          ...messages,
        ],
        stream: false,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        console.error('Rate limit exceeded');
        return new Response(
          JSON.stringify({ error: 'Rate limit exceeded. Please try again later.' }), 
          { status: 429, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
        );
      }
      if (response.status === 402) {
        console.error('Payment required');
        return new Response(
          JSON.stringify({ error: 'AI credits depleted. Please add credits to continue.' }), 
          { status: 402, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
        );
      }
      
      const errorText = await response.text();
      console.error('AI gateway error:', response.status, errorText);
      throw new Error('AI gateway error');
    }

    const data = await response.json();
    const aiResponse = data.choices?.[0]?.message?.content;

    if (!aiResponse) {
      throw new Error('No response from AI');
    }

    console.log('Successfully generated consultation response');

    return new Response(
      JSON.stringify({ response: aiResponse }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );

  } catch (error) {
    console.error('Error in consult-ethics function:', error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : 'Unknown error' }),
      { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  }
});
